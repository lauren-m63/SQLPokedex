package com.example.pokedex;

public class Pokedex {

         int number;
         String name;
         String species;
         String gender;
         double height;
         double weight;
         int level;
         int hp;
         int attack;
         int defense;


    public Pokedex(int number, String name, String species,
                   String gender, double height, double weight,
                   int level, int hp, int attack, int defense) {
        this.number = number;
        this.name = name;
        this.species = species;
        this.gender = gender;
        this.height = height;
        this.weight = weight;
        this.level = level;
        this.hp = hp;
        this.attack = attack;
        this.defense = defense;


}
    }
